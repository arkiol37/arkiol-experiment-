#!/usr/bin/env node
// packages/shared/scripts/generate-prisma-types.cjs
// Generates @prisma/client type stubs from schema.prisma for build-time compilation.
// This is a FALLBACK for environments where `prisma generate` can't run.
// In production builds, always prefer `prisma generate` which produces the real runtime client.

const fs = require('fs');
const path = require('path');

const schemaPath = path.resolve(__dirname, '../prisma/schema.prisma');
const outputPath = path.resolve(__dirname, '../node_modules/@prisma/client/index.d.ts');

console.log('[generate-prisma-types] Reading schema:', schemaPath);

const schema = fs.readFileSync(schemaPath, 'utf8');

// Parse enums
const enumRe = /enum\s+(\w+)\s*\{([^}]+)\}/g;
const enums = [];
let m;
while ((m = enumRe.exec(schema)) !== null) {
  const name = m[1];
  const vals = m[2].trim().split('\n')
    .map(v => v.split('//')[0].trim())
    .filter(v => v && !v.startsWith('@'));
  enums.push(`export enum ${name} { ${vals.map(v => `${v} = "${v}"`).join(', ')} }`);
}

// Parse models
const modelRe = /^model\s+(\w+)\s*\{/gm;
const positions = [];
while ((m = modelRe.exec(schema)) !== null) {
  positions.push({ name: m[1], start: m.index });
}

const typeMap = {
  String: 'string', Int: 'number', Float: 'number',
  Boolean: 'boolean', DateTime: 'Date', BigInt: 'bigint',
  Json: 'JsonValue', Decimal: 'number',
};

const enumNames = new Set(enums.map(e => e.match(/enum (\w+)/)[1]));
const models = [];
const modelNames = [];

for (const { name, start } of positions) {
  let depth = 0;
  const bodyStart = schema.indexOf('{', start);
  let pos = bodyStart;
  while (pos < schema.length) {
    if (schema[pos] === '{') depth++;
    else if (schema[pos] === '}') { depth--; if (depth === 0) break; }
    pos++;
  }
  const body = schema.slice(bodyStart + 1, pos);
  const fields = [];
  for (const line of body.trim().split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('//') || trimmed.startsWith('@@')) continue;
    const parts = trimmed.split(/\s+/);
    if (parts.length < 2 || parts[0].startsWith('@')) continue;
    const fname = parts[0];
    let ftype = parts[1];
    const optional = ftype.endsWith('?');
    ftype = ftype.replace('?', '');
    const isArray = ftype.endsWith('[]');
    if (isArray) ftype = ftype.slice(0, -2);
    let tsType = enumNames.has(ftype) ? ftype : typeMap[ftype];
    if (!tsType) continue; // relation
    if (isArray) tsType += '[]';
    if (optional && !isArray) tsType += ' | null';
    fields.push(`${fname}: ${tsType}`);
  }
  models.push(`export interface Prisma${name} extends PrismaModelBase { ${fields.join('; ')}; }`);
  modelNames.push(name);
}

const delegateName = n => n[0].toLowerCase() + n.slice(1);

const output = `// AUTO-GENERATED from schema.prisma — do not edit manually
// Generated by: packages/shared/scripts/generate-prisma-types.cjs

${enums.join('\n')}

export type JsonValue = string | number | boolean | null | JsonObject | JsonArray;
export interface JsonObject { [key: string]: JsonValue }
export type JsonArray = JsonValue[];

export interface PrismaModelBase { [key: string]: unknown; }

export interface PrismaDelegate<T extends PrismaModelBase> {
  findUnique(args: Record<string, unknown>): Promise<T | null>;
  findUniqueOrThrow(args: Record<string, unknown>): Promise<T>;
  findFirst(args?: Record<string, unknown>): Promise<T | null>;
  findMany(args?: Record<string, unknown>): Promise<T[]>;
  create(args: Record<string, unknown>): Promise<T>;
  update(args: Record<string, unknown>): Promise<T>;
  updateMany(args: Record<string, unknown>): Promise<{ count: number }>;
  upsert(args: Record<string, unknown>): Promise<T>;
  delete(args: Record<string, unknown>): Promise<T>;
  deleteMany(args?: Record<string, unknown>): Promise<{ count: number }>;
  count(args?: Record<string, unknown>): Promise<number>;
  aggregate(args?: Record<string, unknown>): Promise<Record<string, unknown>>;
  groupBy(args: Record<string, unknown>): Promise<Array<Record<string, unknown>>>;
}

${models.join('\n')}

export type TxClient = Omit<PrismaClient, '$connect' | '$disconnect' | '$on' | '$transaction' | '$use' | '$extends'>;

export declare class PrismaClient {
  constructor(opts?: Record<string, unknown>);
  $connect(): Promise<void>;
  $disconnect(): Promise<void>;
  $on(event: string, listener: (...args: unknown[]) => void): void;
  $use(middleware: (params: unknown, next: (params: unknown) => Promise<unknown>) => Promise<unknown>): void;
  $extends(extension: unknown): PrismaClient;
  $transaction<T>(fn: (prisma: TxClient) => Promise<T>, options?: Record<string, unknown>): Promise<T>;
  $transaction<T>(args: Promise<T>[], options?: Record<string, unknown>): Promise<T[]>;
  $queryRaw(query: TemplateStringsArray, ...values: unknown[]): Promise<unknown>;
  $executeRaw(query: TemplateStringsArray, ...values: unknown[]): Promise<number>;
${modelNames.map(n => `  ${delegateName(n)}: PrismaDelegate<Prisma${n}>;`).join('\n')}
}

export declare namespace Prisma {
  type JsonValue = string | number | boolean | null | JsonObject | JsonArray;
  interface JsonObject { [key: string]: JsonValue }
  type JsonArray = JsonValue[];
}

export default PrismaClient;
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, output);
console.log('[generate-prisma-types] Written:', outputPath);
console.log('[generate-prisma-types] Models:', modelNames.length, 'Enums:', enums.length);
